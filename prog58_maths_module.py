import math
# for maths constants such as pi and e
print(math.pi)
print(math.e)

# trignometry functions
a = math.pi/4
print(math.tan(a))
# .tan(x) and .tan2(x, y) are different
# .tan2(x, y) takes sign into consideration and ranges from -pi
# to +pi

# to convert to degree
print(math.degrees(a))

# to radians
b = 30
print(math.radians(b))

